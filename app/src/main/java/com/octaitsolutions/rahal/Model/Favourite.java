package com.octaitsolutions.rahal.Model;

import java.io.Serializable;

public class Favourite implements Serializable {

    String id;
    int image;
    int name;

    public Favourite(String id, int image, int name) {
        this.id = id;
        this.image = image;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }
}
